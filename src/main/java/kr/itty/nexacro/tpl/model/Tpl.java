package kr.itty.nexacro.tpl.model;

public class Tpl {
	private String pjtNo;
	private String tplCo;
	private String tplCont;
	private String tplCons;
	private String tplP;
	private String pjtType;
	
	public String getPjtNo() {
		return pjtNo;
	}
	public void setPjtNo(String pjtNo) {
		this.pjtNo = pjtNo;
	}
	public String getTplCo() {
		return tplCo;
	}
	public void setTplCo(String tplCo) {
		this.tplCo = tplCo;
	}
	public String getTplCont() {
		return tplCont;
	}
	public void setTplCont(String tplCont) {
		this.tplCont = tplCont;
	}
	public String getTplCons() {
		return tplCons;
	}
	public void setTplCons(String tplCons) {
		this.tplCons = tplCons;
	}
	public String getTplP() {
		return tplP;
	}
	public void setTplP(String tplP) {
		this.tplP = tplP;
	}
	public String getPjtType() {
		return pjtType;
	}
	public void setPjtType(String pjtType) {
		this.pjtType = pjtType;
	}
}
